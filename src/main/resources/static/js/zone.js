function is_checked() {

    let checkbox = document.getElementById('onOffLine');

    let is_checked = checkbox.checked;

    if(is_checked === true) {
      $("#firstZone").attr("disabled", true);
      $("#secondZone").attr("disabled", true);
    } else {
      $("#firstZone").removeAttr("disabled");
      $("#secondZone").removeAttr("disabled");
    }
}
